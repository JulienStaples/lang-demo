export function findDiff(word) {
  try {
    return JSON.parse(sessionStorage.getItem(word)).diff
  } catch {
    return "uk"
  }
}

export function findEntry(word) {
  if (JSON.parse(sessionStorage.getItem(word)))
    return JSON.parse(sessionStorage.getItem(word))
  else return ""
}
